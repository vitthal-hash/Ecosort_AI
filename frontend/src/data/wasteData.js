// ── Helper: compute real CO₂ and energy for a list of confirmed items ─────────
// Used by AppContext.finaliseSession()
export function computeImpact(itemsWithClass) {
  let co2 = 0;
  let energy = 0;
  itemsWithClass.forEach(item => {
    const cls = (item.class || "").toLowerCase();
    const impact = ECO_IMPACT[cls];
    if (impact) {
      co2    += impact.co2PerItem    || 0;
      energy += impact.energyPerItem || 0;
    } else {
      // Fallback for unknown classes — conservative estimate
      co2    += 0.5;
      energy += 0.3;
    }
  });
  return {
    co2SavedKg:     parseFloat(co2.toFixed(2)),
    energySavedKWh: parseFloat(energy.toFixed(2)),
  };
}